     private void Show_data(String query) throws ClassNotFoundException{
        DefaultTableModel model1 = new DefaultTableModel();
        model1.addColumn("ID");
        model1.addColumn("KODE");
        model1.addColumn("NAMA");
        model1.addColumn("JENIS");
        try{
            int no = 1;
            String sql = query;
            java.sql.Connection db = (Connection) Koneksi.configDB();
            java.sql.Statement stm;
            stm = db.createStatement();
            java.sql.ResultSet result = stm.executeQuery(sql);
            while(result.next()){
                model1.addRow(new Object[]{result.getString(1), result.getString(2), result.getString(3), result.getString(4)});
            }
            tObat.setModel(model1);
        }catch(SQLException e){
            System.out.println("Error : "+e.getMessage());
        }
        TableColumn column;
       tObat.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_OFF);
       column = tObat.getColumnModel().getColumn(0);
       column.setPreferredWidth(30);
        column = tObat.getColumnModel().getColumn(1);
       column.setPreferredWidth(140);
        column = tObat.getColumnModel().getColumn(2);
       column.setPreferredWidth(100);
       column = tObat.getColumnModel().getColumn(3);
       column.setPreferredWidth(140);
     }
    
   
