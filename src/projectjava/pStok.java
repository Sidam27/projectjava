    private void bSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSubmitActionPerformed
        // TODO add your handling code here:
        int banyak = model.getRowCount();
        String namap = tNamasupplier.getText();
        Penyetokan stok = new Penyetokan();
        stok.tambahData(namap, tharga);
        for(int i = 0;i<banyak;i++){
            String io = tPilihObat.getValueAt(i, 0).toString();
            int ido = Integer.valueOf(io);
            String jum =tPilihObat.getValueAt(i, 2).toString();
            int jumlaho = Integer.valueOf(jum);
            String har = tPilihObat.getValueAt(i, 3).toString();
            int hargao = Integer.valueOf(har);
            stok.tambahDetail(ido, jumlaho, hargao);
        }

    }//GEN-LAST:event_bSubmitActionPerformed

